
public class Student {

	
	private int id;
	private String name;
	
	public int getId() { //getter method
		
		return id;
	}
	
	public String getName() {
		
		return name;
	}
	
	public void setId(int id) { //setter method
		
	this.id=id;
	}
	
	public void setName(String name) {
		
		this.name=name;
	}
}
